#include <stdio.h>

int main(){
    int a = 10 ; 
    int b = 5; 

    // Addition Operator 
    int sum = a+b ; 
    printf("Sum of two number: %d\n", sum);

    // Substract 
    int sub = a - b ; 
    printf("Sub of two number: %d\n", sub); 

    // multiplication 
    int mul = a * b; 
    printf("Mul of two number: %d\n", mul); 

    // Division 
    float x = 10; 
    int y = 5; 

    float div = x / y; 
    printf("Div of two number: %.2f\n", div);

    // Mod 
    int c = 15;  
    int d = 4;

    int mod = 15 % 4 ; 
    printf("Mod of two number: %d\n", mod); 

    
    // Logical Operator >> && (Logical and), ||(logical or), ! (logical not)
    // Relational Operator = >,<,>=,<= ==, != 

    return 0; 
}